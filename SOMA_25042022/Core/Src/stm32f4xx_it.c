	FatFsCnt++;
	if(FatFsCnt >= 10)
	{
		FatFsCnt = 0;
		SDTimer_Handler();
	}

	// m16946 added
	msTicks ++ ;
	runCnt ++ ;
  /* USER CODE END TIM5_IRQn 0 */
  HAL_TIM_IRQHandler(&htim5);
